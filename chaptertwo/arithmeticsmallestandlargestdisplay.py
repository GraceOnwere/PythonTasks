first_number = int (input('Enter first number: '))

second_number = int (input('Enter second number: '))

third_number = int (input('Enter third number: '))

addition = first_number + second_number + third_number

average = (first_number + second_number + third_number) / 3

product = first_number * second_number * third_number

smallest = min(first_number, second_number, third_number)

largest =  max(first_number, second_number, third_number)

print('The addition of the numbers is ',addition)
print('The average of the numbers is ',average)
print('The product of the numbers is ',product)
print('The smallest among the numbers is ',smallest)
print('The largest among the numbers is ',largest)
